package kr.ac.ajou.lazybones.templates;


public class HouseStatus {
	
	private Character dsw;
	private Character dsv;
	private Character alr;
	private Character lgt;
	private Double hum;
	private Double tmp;
	private Character occ;
	
	public HouseStatus(){
		
	}

	public Character getDsw() {
		return dsw;
	}

	public void setDsw(Character dsw) {
		this.dsw = dsw;
	}

	public Character getDsv() {
		return dsv;
	}

	public void setDsv(Character dsv) {
		this.dsv = dsv;
	}

	public Character getAlr() {
		return alr;
	}

	public void setAlr(Character alr) {
		this.alr = alr;
	}

	public Character getLgt() {
		return lgt;
	}

	public void setLgt(Character lgt) {
		this.lgt = lgt;
	}

	public Double getHum() {
		return hum;
	}

	public void setHum(Double hum) {
		this.hum = hum;
	}

	public Double getTmp() {
		return tmp;
	}

	public void setTmp(Double tmp) {
		this.tmp = tmp;
	}

	public Character getOcc() {
		return occ;
	}

	public void setOcc(Character occ) {
		this.occ = occ;
	}
	
	
	

}
